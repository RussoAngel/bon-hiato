    private void accAddResourceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accAddResourceActionPerformed
        // TODO add your handling code here:
        if (nameAddResource.getText().equals("") || descriptionAddResource.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Se debe indicar un nombre y una descripción.");
        } else if (getPrice() < 0) {
            JOptionPane.showMessageDialog(null, "El precio no puede ser negativo");
        } else if (getUnidades() < 0) {
            JOptionPane.showMessageDialog(null, "Las unidades no pueden ser inferiores a 0");
        } else {
            try {

                String query = " insert into resource (id, name, description, unidad, project, precio)"
                        + " values (?, ?, ?, ?, ?, ?)";

                // create the mysql insert preparedstatement
                PreparedStatement preparedStmt = connection.prepareStatement(query);
                preparedStmt.setInt(1, identificador.getId());
                preparedStmt.setString(2, nameAddResource.getText());
                preparedStmt.setString(3, descriptionAddResource.getText());
                preparedStmt.setInt(4, Integer.parseInt(unidadAddResource.getText()));
                preparedStmt.setString(5, project);
                preparedStmt.setFloat(6, Float.parseFloat(precioAddResource.getText()));

                // execute the preparedstatement
                preparedStmt.execute();
                JOptionPane.showMessageDialog(null, "Se ha añadido con éxito el recurso "+ nameAddResource.getText());
            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(null, e);
            }
            SelectResource sele = new SelectResource(project, developer2);
            sele.setLocationRelativeTo(null);
            sele.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_accAddResourceActionPerformed

