    private void accCreateProjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accCreateProjectActionPerformed
        // TODO add your handling code here:
        if(nameCreateProject.getText().equals("") || descriptionCreateProject.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Se deben rellenar todos los campos.");
        }else{
            try{
                String query = " insert into projects (name, Duration, Description)"
                + " values (?, ?, ?)";

                // create the mysql insert preparedstatement
                PreparedStatement preparedStmt = connection.prepareStatement(query);
                preparedStmt.setString(1, nameCreateProject.getText());
                preparedStmt.setInt(2, 0);
                preparedStmt.setString(3, descriptionCreateProject.getText());

                // execute the preparedstatement
                preparedStmt.execute();
                JOptionPane.showMessageDialog(null,"Se ha creado el proyecto "+nameCreateProject.getText()+" con éxito");

            }catch(SQLException | HeadlessException e){
                JOptionPane.showMessageDialog(null, e);
            }
            
            ProductOwner pr = new ProductOwner(develop);
            pr.setLocationRelativeTo(null);
            pr.setVisible(true);
            dispose();
        
        }
    }//GEN-LAST:event_accCreateProjectActionPerformed

